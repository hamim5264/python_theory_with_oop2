"""
Two ways to add item in list
1. use append function (where item will be added in last position)
2. use insert function (where item will be added in specific position)
"""

# Using append function
myProgrammingList = ["C", "C#", "C++", "Java"]
print(myProgrammingList)
myProgrammingList.append("Python")
print(myProgrammingList)

# Using insert function
myNumberList = [1, 2, 3, 4, 5]
print(myNumberList)
myNumberList.insert(1, 10)
print(myNumberList)
