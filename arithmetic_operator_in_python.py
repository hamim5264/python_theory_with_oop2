# Arithmetic operator
a = 10
b = 3
print(f"Addition is: {a + b}")
print(f"Subtraction is: {a - b}")
print(f"Multiplication is: {a * b}")
print(f"Division is: {a / b}")

# Exponentiation
"""
Exponentiation means it will multiply x depends on y
for example:
X = 5
Y = 2
X**Y
That means X will be multiply 2 times with its own value
5*5
"""

x = 5
y = 3
print(f"Exponentiation is: {x ** y}")

# Floor Division
"""
Floor division means it will print the int value of division
"""
num1 = 10
num2 = 4
print(f"Without Floor Division: {num1 / num2}")
print(f"With Floor Division: {num1 // num2}")
