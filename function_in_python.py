"""
4 types of function are:
1. Build-in function
2. Recursive function
3. Lambda function
4. User define function
Note : always use def keyword to make function
"""


# Example of sum function
def sumOfTwoNumbers(a, b):
    total = a + b
    print("Sum of two numbers: ", total)


# Example of subtract function
def subOfTwoNumbers(a, b):
    total = a - b
    print("Sub of two numbers: ", total)


sumOfTwoNumbers(2, 5)
subOfTwoNumbers(20, 5)
