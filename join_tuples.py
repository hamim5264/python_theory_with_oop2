programsTuple1 = ("C", "C#", "C++", "Python", "Java")
programsTuple2 = ("Dart", "PHP", "Rubby", "GoLang")
print("First tuple: ", programsTuple1)
print("Second tuple: ", programsTuple2)

# Join two tuples using + sign
additionTuple = programsTuple1 + programsTuple2
print("Joined tuple: ", additionTuple)

# Multiply tuples
multiplyTuples1 = programsTuple1 * 2
multiplyTuples2 = programsTuple2 * 3
print(multiplyTuples1)
print(multiplyTuples2)
