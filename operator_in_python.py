# Exponentiation
"""
Exponentiation means it will multiply x depends on y
for example:
X = 5
Y = 2
X**Y
That means X will be multiply 2 times with its own value
5*5
"""

x = 5
y = 3
exponentiationSum = x ** y
print(f"Exponentiation is: {exponentiationSum}")

# Floor Division
"""
Floor division means it will print the int value of division
"""
num1 = 10
num2 = 4
withoutFloorDivision = num1 / num2
floorDivisionSum = num1 // num2
print(f"Without Floor Division: {withoutFloorDivision}")
print(f"With Floor Division: {floorDivisionSum}")
