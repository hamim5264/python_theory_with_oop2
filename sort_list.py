# Sort by ascending
numberList = [1, 5, 2, 3, 10, 4, 7]
print("Before sorting: ", numberList)
numberList.sort()
print("After sorting", numberList)

# Sort by alphabetic
alphabeticList = ["Orange", "Apple", "Sweet", "Bread", "Banana"]
print("Before sorting: ", alphabeticList)
alphabeticList.sort()
print("After Sorting: ", alphabeticList)

# Sort by reverse
numberList = [1, 5, 2, 3, 10, 4, 7]
print("Before reverse: ", numberList)
numberList.reverse()
print("After reverse", numberList)

# Sort with reverse
numberList = [1, 5, 2, 3, 10, 4, 7]
print("Before reverse and sort: ", numberList)
numberList.sort(reverse=True)
print("After reverse and sort: ", numberList)
